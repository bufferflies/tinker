package data

import (
	"io"

	v12 "k8s.io/api/core/v1"
	"k8s.io/client-go/kubernetes"
	"k8s.io/client-go/kubernetes/scheme"
	"k8s.io/client-go/rest"
	"k8s.io/client-go/tools/remotecommand"
)

// exec
func exec(podName, container, namespace string, command []string, config *rest.Config, stdout io.Writer) error {
	k8sCli, err := kubernetes.NewForConfig(config)
	if err != nil {
		return err
	}

	req := k8sCli.CoreV1().RESTClient().Post().Namespace(namespace).
		Name(podName).Resource("pods").SubResource("exec")
	option := &v12.PodExecOptions{
		Command:   command,
		Container: container,
		Stdin:     false,
		Stdout:    true,
		Stderr:    true,
		TTY:       true,
	}

	req.VersionedParams(
		option,
		scheme.ParameterCodec,
	)
	exec, err := remotecommand.NewSPDYExecutor(config, "POST", req.URL())
	if err != nil {
		return err
	}
	err = exec.Stream(remotecommand.StreamOptions{
		Stdin:  nil,
		Stdout: stdout,
	})
	return err
}
